public class TipoAnuncio {

    private int idtipoanuncio ;
    private String nome;
    private int qtdpalavras;

    /**
     * @return the idtipoanuncio
     */
    public int getIdtipoanuncio() {
        return idtipoanuncio;
    }

    /**
     * @param idtipoanuncio the idtipoanuncio to set
     */
    public void setIdtipoanuncio(int idtipoanuncio) {
        this.idtipoanuncio = idtipoanuncio;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the qtdpalavras
     */
    public int getQtdpalavras() {
        return qtdpalavras;
    }

    /**
     * @param qtdpalavras the qtdpalavras to set
     */
    public void setQtdpalavras(int qtdpalavras) {
        this.qtdpalavras = qtdpalavras;
    }

}
